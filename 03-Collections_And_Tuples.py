def readData(dataPath) :
    dataFile = open(dataPath)
    dataStr = dataFile.read()
    dataList = dataStr.splitlines()
    return dataList
	
orders = readData("/data/retail_db/orders/part-00000")	

order_statuses = set({})

for i in orders:
	order_statuses.add(i.split(",")[3]) 

  
